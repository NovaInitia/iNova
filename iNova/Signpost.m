#import "Signpost.h"

@implementation Signpost

@synthesize idNum = _idNum, user = _user, title = _title, comment = _comment, nsfw = _nsfw;

- (id) initWithID:(NSUInteger)idNum user:(NSString *)user title:(NSString *)title comment:(NSString *)comment nsfw:(BOOL)nsfw
{
    self = [super init];
    if (self)
    {
        _idNum = idNum;
        _user = user;
        _title = title;
        _comment = comment;
        _nsfw = nsfw;
    }
    return self;
}


@end
